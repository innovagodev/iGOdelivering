export type TranslationKey = keyof typeof translations.it;

export function getTranslation(lang: Lang, key: TranslationKey, vars?: Record<string, string | number>): string {
  const dictionary = translations[lang] || translations.it;
  let text = (dictionary[key] || translations.it[key] || key) as string;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}
